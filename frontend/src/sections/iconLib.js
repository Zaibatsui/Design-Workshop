/**
 * Returns inline SVG markup for the given key, sized at `size` px and
 * coloured via currentColor. Returns an empty string for "none" / unknown.
 */
export function svgIcon(key, size = 24) {
  const inner = ICON_LIBRARY[key] || "";
  if (!inner) return "";
  return `<svg width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${inner}</svg>`;
}
